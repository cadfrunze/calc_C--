#include <iostream>
#include <math.h>
using namespace std;

// Operatiile matematice

double adunare(double n1, double n2) {
	/*
	functie adunare
	*/
	return n1 + n2;
}

double scadere(double n1, double n2) {
	/*
	functie scadere
	*/
	return n1 - n2;
}

double impartire(double n1, double n2) {
	/*
	functie scadere
	*/
	return n1 / n2;
}

double inmultire(double n1, double n2) {
	/*
	functie inmultire
	*/
	return n1 * n2;
}

double laPutere(double n1, double n2) {
	/*
	functie la puterea
	*/
	return pow(n1, n2);
}